--firebird

CREATE TABLE empresas
(
    id integer generated by default as identity primary key,
    cnpj varchar(16),
    fantasia varchar(155),
    tipo integer,
    situacao varchar(10),
    datasituacao date,
    motivosituacao varchar(2),
    nomecidadeexterior varchar(100),
    pais varchar(10),
    inicioatividade date,
    cnaeprincipal varchar(8),
    tipologradouro varchar(50),
    logradouro varchar(150),
    numero varchar(20),
    complemento varchar(100),
    bairro varchar(70),
    cep varchar(10),
    uf varchar(2),
    municipio int,
    telefoneprincipal varchar(15),
    telefonesecundario varchar(15),
    email varchar(150),
    situacaoespecial varchar(50),
    datasituacaoespecial date
);

